import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";
import { imageSize } from "image-size";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, "..");
const photosDir = path.resolve(repoRoot, "public", "photos");
const outFile = path.resolve(repoRoot, "components", "photos", "photosManifest.ts");

const exts = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif", ".gif"]);

function toAltText(fileName) {
  const base = fileName.replace(path.extname(fileName), "");
  return base
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function listPhotosFlat(dirPath) {
  if (!fs.existsSync(dirPath)) return [];
  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((d) => d.isFile())
    .map((d) => d.name)
    .filter((name) => exts.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
}

const files = listPhotosFlat(photosDir);

ensureDir(path.dirname(outFile));

const entries = files.map((fileName) => {
  const abs = path.resolve(photosDir, fileName);
  const buf = fs.readFileSync(abs);
  const dim = imageSize(buf);

  if (!dim.width || !dim.height) {
    throw new Error(`Could not read dimensions for ${fileName}`);
  }

  return {
    src: `/photos/${fileName}`,
    width: dim.width,
    height: dim.height,
    alt: toAltText(fileName),
  };
});

const header = `// This file is auto-generated by scripts/generate-photos-manifest.mjs
// Do not edit by hand.

export type Photo = {
  src: string;
  width: number;
  height: number;
  alt: string;
};

export const photos: Photo[] = `;

const body = JSON.stringify(entries, null, 2)
  .replace(/\"([^\"]+)\":/g, "$1:")
  .replace(/\n]/g, "\n]");

fs.writeFileSync(outFile, `${header}${body} as const;\n`, "utf8");

process.stdout.write(
  `Wrote ${entries.length} photo${entries.length === 1 ? "" : "s"} to ${path.relative(
    repoRoot,
    outFile
  )}\n`
);
